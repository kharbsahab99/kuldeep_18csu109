class Qualification
{
	String qualName;
	String university;
	String institute;
	float cgpa;
	
	void setqualName(String qualName)
	{
		this.qualName=qualName;
	}
	String getqualName()
	{
		return qualName;
	}
	
	void setuniversity(String university)
	{
		this.university=university;
	}
	String getuniversity()
	{
		return university;
	}
	
	void setinstitute(String institute)
	{
		this.institute=institute;
	}
	String getinstitute()
	{
		return institute;
	}
	
	void setcgpa(float cgpa)
	{
		this.cgpa=cgpa;
	}
	float getcgpa()
	{
		return cgpa;
	}
}